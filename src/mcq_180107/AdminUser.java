    boolean checkUser(String id,String username) throws SQLException{
           ResultSet rs;
           rs = Connect.getResult("select * from users where id <>"+id+" AND username='"+username+"' ");
           return (rs.next());
    }
    
    boolean checkUserDup(String username) throws SQLException{
        ResultSet rs;
        rs = Connect.getResult("select * from users where username='"+username+"' ");
        return (rs.next());
    }
    
    boolean checkField(){
        if(txtUser.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Username cannot be empty.", "Error", JOptionPane.ERROR_MESSAGE);
            return false;	 
        }
        if(txtToken.getText().equals("")){
            JOptionPane.showMessageDialog(null, "Please generate token before saving", "Error", JOptionPane.ERROR_MESSAGE);
            return false;	 
        }
        return true; 
    }
    
    void loadData()
    {
        
       try {
           ResultSet rs;
           rs = Connect.getResult("select * from users where is_admin =0");
           DefaultTableModel model = (DefaultTableModel) table.getModel();
           while(rs.next())
           {
            model.addRow(new Object[]{rs.getString(1), rs.getString(2), 
            rs.getString(3)});
               
           }
           
           
           
           
 
           
       } catch (SQLException ex) {
           Logger.getLogger(AdminUser.class.getName()).log(Level.SEVERE, null, ex);
           ex.printStackTrace();
       }
        
    }

    public String tokenGenerator()
    {
        String charList = "ABCDEFGHIJKLMNOPQRSTUVWXYZ0123456789abcdefghijklmnopqrstuvxyz"; 

    StringBuilder token = new StringBuilder(6); 

    for (int i = 0; i < 6; i++) { 

        int index 
            = (int)(charList.length() * Math.random()); 

        token.append(charList.charAt(index)); 
    } 

    return token.toString(); 

    }
