    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        if(operation==1){
            Connection con= Connect.getConnection();
            boolean chk=true;
            try {
                chk=checkUser(userId,txtUser.getText());
            } catch (SQLException ex) {
                Logger.getLogger(AdminUser.class.getName()).log(Level.SEVERE, null, ex);
                return;
            }
            System.out.println(chk);
            if(checkField()){
                if (chk){
                JOptionPane.showMessageDialog(null, "Username already taken. Please choose another username.", "Error", JOptionPane.ERROR_MESSAGE);
		return;		
                }
                else{
                   try {

                    PreparedStatement pst= con.prepareStatement("update  users set username=?,password=? where id=?");
                    pst.setString(1, txtUser.getText());
                    pst.setString(2, txtToken.getText());

                    pst.setString(3, userId);
                    pst.executeUpdate();




                    } catch (SQLException ex) {
                      Logger.getLogger(AdminUser.class.getName()).log(Level.SEVERE, null, ex);
                      ex.printStackTrace();


                    }
                    DefaultTableModel model = (DefaultTableModel) table.getModel();

                    model.setValueAt(userId, row, 0);
                    model.setValueAt(txtUser.getText(), row, 1);
                    model.setValueAt(txtToken.getText(), row, 2);
                    JOptionPane.showMessageDialog(null, "Successfully updated", "Updated", JOptionPane.INFORMATION_MESSAGE);
                    reset();
                    return;
                }
            }else return;
            
            
//       
            
        }
        else{
            if(checkField()){
                boolean chk=true;
                try {
                    chk=checkUserDup(txtUser.getText());
                } catch (SQLException ex) {
                    Logger.getLogger(AdminUser.class.getName()).log(Level.SEVERE, null, ex);
                    return;
                }
                System.out.println(chk);
                if (chk){
                    JOptionPane.showMessageDialog(null, "Username already taken. Please choose another username.", "Error", JOptionPane.ERROR_MESSAGE);
                    return;		
                }else{
                    Connection con= Connect.getConnection();
                    PreparedStatement pst;
                    try {
                        pst = con.prepareStatement("insert into users (username,password,is_admin) values (?,?,?)");
                        pst.setString(1, txtUser.getText());
                        pst.setString(2, txtToken.getText());
                        pst.setString(3, "0");
                        pst.execute();

                    } catch (SQLException ex) {
                        Logger.getLogger(AdminUser.class.getName()).log(Level.SEVERE, null, ex);
                        return;
                    }
                    ResultSet rs;
                    int last_inserted_id=0;
                    try {
                        rs = pst.getGeneratedKeys();
                        if(rs.next())
                        {
                            last_inserted_id = rs.getInt(1);
                        }
                    } catch (SQLException ex) {
                        Logger.getLogger(AdminUser.class.getName()).log(Level.SEVERE, null, ex);
                    }

                    DefaultTableModel model = (DefaultTableModel) table.getModel();
                    model.addRow(new Object[]{last_inserted_id, txtUser.getText(), 
                    txtToken.getText()}); 
                    JOptionPane.showMessageDialog(null, "User Added Successfully", "Added", JOptionPane.INFORMATION_MESSAGE);
                    reset();
                    return;


                }
            }
            
        }
            
    }//GEN-LAST:event_btnSaveActionPerformed

